//Create and store Pokemon attacks.

public class Attack{
	private int damage, cost;
	private String name, special;
	
	public Attack(String name, int cost, int damage, String special){
		this.name = name;
		this.damage = damage;
		this.cost = cost;
		this.special = special;
	}
	
	
	
}